"""Canonical compound catalog for the allergies app.

Defines every skincare-relevant compound as a CompoundEntry row.
This module is the single source of truth for allergen keys, labels,
INCI names, and subcategory groupings.

Exports:
    COMPOUNDS          — tuple of all CompoundEntry rows
    CompoundEntry       — NamedTuple type for a single compound
    FLAT_ALLERGEN_LABEL_MAP — dict[str, str] mapping allergen_key → display_label
    CATEGORY_CHOICES    — single-entry list for the Allergen.category field
    CATEGORY_CONTACT    — the lone category key ("contact")

Do not import from allergies.constants.choices — this module replaces it.

Reference source:
    INCI names and CAS numbers are verified against the EU CosIng
    (Cosmetic Ingredient) database maintained by the European Commission:
    https://ec.europa.eu/growth/tools-databases/cosing/

    INCIDecoder (incidecoder.com) is used as a secondary cross-reference
    to confirm which INCI names appear on real product labels.

    When CosIng lists multiple CAS numbers for a single INCI name, the
    primary CAS (first listed in CosIng) is used unless a more specific
    one applies to the cosmetic use case.

    When a single allergen concern spans multiple INCI-distinct species
    or derivatives (e.g. Cymbopogon Citratus vs Flexuosus vs Schoenanthus
    for lemongrass, or Hydrolyzed Soy Protein vs Glycine Soja Extract for
    soy), this catalog keeps one CompoundEntry per chemically distinct
    substance. Variant names are stored in common_names for future alias
    resolution. User-facing allergen grouping ("I'm allergic to soy") is
    resolved by the AllergenAlias layer, not by merging entries here.
    See STATUS.md § Compound Catalog Design Decisions for full rationale.
"""

from typing import Final, NamedTuple

# ============================================================================
# Category constants — single-category catalog
# ============================================================================

CATEGORY_CONTACT: Final[str] = "contact"

CATEGORY_CHOICES: Final[list[tuple[str, str]]] = [
    (CATEGORY_CONTACT, "Contact/Topical Allergens"),
]


# ============================================================================
# CompoundEntry definition
# ============================================================================


class CompoundEntry(NamedTuple):
    """A single compound in the allergen catalog.

    Fields:
        key:            Canonical allergen_key stored in the DB.
                        Lowercase, underscored, no special characters.
        inci_name:      International Nomenclature of Cosmetic Ingredients name.
                        Empty string if no standard INCI name exists.
        display_label:  Human-readable label shown in the UI.
        category:       Always "contact" in the current catalog.
        subcategory:    Grouping for checkbox display (e.g. "Fragrances").
        common_names:   Tuple of alternate names for future synonym mapping.
        cas_number:     CAS Registry Number. Empty string if unknown.
        eu_annex_iii:   True if listed in EU Cosmetics Regulation Annex III.
        regulatory_ref: Regulation reference string. Empty if none.
    """

    key: str
    inci_name: str
    display_label: str
    category: str
    subcategory: str
    common_names: tuple[str, ...]
    cas_number: str
    eu_annex_iii: bool
    regulatory_ref: str


