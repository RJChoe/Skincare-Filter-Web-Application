def _assert_entries_well_formed(compounds: tuple[CompoundEntry, ...]) -> None:
    """Validate that every CompoundEntry has required non-empty fields."""
    for i, entry in enumerate(compounds):
        if not isinstance(entry, CompoundEntry):
            raise AssertionError(
                f"Entry at index {i} is not a CompoundEntry: {entry!r}"
            )
        if not entry.key:
            raise AssertionError(f"Entry at index {i} has empty key: {entry!r}")
        if not entry.display_label:
            raise AssertionError(
                f"Entry at index {i} has empty display_label: {entry!r}"
            )
        if not entry.category:
            raise AssertionError(f"Entry at index {i} has empty category: {entry!r}")
        if not entry.subcategory:
            raise AssertionError(f"Entry at index {i} has empty subcategory: {entry!r}")
        # Validate key format: lowercase, underscored, no special chars
        if entry.key != entry.key.lower():
            raise AssertionError(f"Key {entry.key!r} at index {i} is not lowercase")
        if " " in entry.key or "-" in entry.key or "'" in entry.key:
            raise AssertionError(
                f"Key {entry.key!r} at index {i} contains spaces, hyphens, "
                "or apostrophes"
            )


def _assert_keys_unique(compounds: tuple[CompoundEntry, ...]) -> None:
    """Validate that all allergen keys are unique across the entire catalog."""
    seen: dict[str, str] = {}  # key -> subcategory
    duplicates: list[str] = []

    for entry in compounds:
        if entry.key in seen:
            duplicates.append(
                f"  {entry.key!r}: first in {seen[entry.key]!r},"
                f" also in {entry.subcategory!r}"
            )
        else:
            seen[entry.key] = entry.subcategory

    if duplicates:
        raise AssertionError(
            "Duplicate allergen keys detected:\n" + "\n".join(duplicates)
        )


